x = int(input("Enter value of x: "))
y = int(input("Enter value of y: "))

#ouput: x+y=    addition
print('x + y =', x+y)
#ouput: x-y=    subtraction
print('x - y =', x-y)
#ouput: x*y=    multiplication
print('x * y =', x*y)
#ouput: x/y=    quotient and remainder
print('x / y =', x/y)
#ouput: x//y=  floor division
print('x // y =', x//y)
#ouput: x**y=   raised to power
print('x ** y =', x**y)
#ouput: x%y=    modulus
print('x % y =', x%y)